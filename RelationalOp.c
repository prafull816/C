// RELATIONAL OPERATORS //

#include<stdio.h>
int main()
{
    int a,b;
    printf("Enter Two Numbers:");
    scanf("%d %d",&a,&b);
    printf("\n a>b is = %d",a>b);
    printf("\n a<b is = %d",a<b);
    printf("\n a>=b is = %d",a>=b);
    printf("\n a<=b is = %d",a<=b);
    printf("\n a==b is = %d",a==b);
    printf("\n a!=b is = %d",a!=b);
    return 0;
}