// ARITHMETICAL OPERATORS //

#include<stdio.h>
int main()
{
    int a,b;
    printf("Enter Two Numbers:");
    scanf("%d %d",&a,&b);
    printf("\nAddition=%d",a+b);
    printf("\nSubtraction=%d",a-b);
    printf("\nMultiplication=%d",a*b);
    printf("\nDividation=%d",a/b);
    printf("\nModulus=%d",a%b);
    return 0;
}